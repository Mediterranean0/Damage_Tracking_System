    private void aracIdTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_aracIdTxtKeyReleased
        char[] a = aracIdTxt.getText().toCharArray();
        int sayac = 0;
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < a.length; i++) {
            if (Character.isLetter(a[i])) {
                
                if (sayac == 0) {
                    sayac++;
                    JOptionPane.showMessageDialog(rootPane, "Id kısmına harf girilemez", "Hata", JOptionPane.WARNING_MESSAGE);
                    
                }
                
            } else {
                sb.append(a[i]);
            }
            aracIdTxt.setText(String.valueOf(sb));
        }
    }//GEN-LAST:event_aracIdTxtKeyReleased


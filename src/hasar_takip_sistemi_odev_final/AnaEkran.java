    private void aracEkleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aracEkleBtnActionPerformed
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("odev");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();
        Query q = em.createQuery("select m from Musteri m where m.id= " + Integer.parseInt(idAracTxt.getText()));
        if (!q.getResultList().isEmpty()) {
            
            if (!idAracTxt.getText().isEmpty() && !aracIdTxt.getText().isEmpty() && !modelList.isSelectionEmpty()) {
                try {
                    if (aracTurCombo.getSelectedItem().toString().equals("Araba")) {
                        
                        Araba a = new Araba();
                        
                        a.setAracId(Integer.parseInt(aracIdTxt.getText()));
                        a.setModel(modelList.getSelectedValue().toString());
                        
                        a.setMusteriId(Integer.parseInt(idAracTxt.getText()));
                        
                        a.setTarih(d);
                        em.persist(a);
                        em.getTransaction().commit();
                        
                    } else if (aracTurCombo.getSelectedItem().toString().equals("Kamyon")) {
                        Kamyon a = new Kamyon();
                        a.setAracId(Integer.parseInt(aracIdTxt.getText()));
                        a.setModel(modelList.getSelectedValue());
                        a.setMusteriId(Integer.parseInt(idAracTxt.getText()));
                        a.setTarih(d);
                        em.persist(a);
                        em.getTransaction().commit();
                        
                    } else if (aracTurCombo.getSelectedItem().toString().equals("Motorsiklet")) {
                        Motorsiklet a = new Motorsiklet();
                        a.setAracId(Integer.parseInt(aracIdTxt.getText()));
                        a.setModel(modelList.getSelectedValue());
                        a.setMusteriId(Integer.parseInt(idAracTxt.getText()));
                        a.setTarih(d);
                        em.persist(a);
                        em.getTransaction().commit();
                    }
                } catch (javax.persistence.RollbackException e) {
                    JOptionPane.showMessageDialog(rootPane, "Girilen id ile bir araç mevcut", "Hata", JOptionPane.WARNING_MESSAGE);
                    
                }
                
            } else {
                JOptionPane.showMessageDialog(rootPane, "Id alanları boş kalamaz ve bir araç modeli seçilmelidir", "Hata", JOptionPane.WARNING_MESSAGE);
                
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Girilen id ile eşleşen müşteri bulunamadı", "Hata", JOptionPane.WARNING_MESSAGE);
        }
        em.close();
        emf.close();
    }//GEN-LAST:event_aracEkleBtnActionPerformed


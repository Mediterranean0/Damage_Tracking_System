    private void kayıtBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kayıtBtnActionPerformed
        Pattern p = Pattern.compile("[0-9]");
        Matcher m = p.matcher(adiTxt.getText());
        Matcher m1 = p.matcher(soyTxt.getText());
        
        Pattern p1 = Pattern.compile("[A-z]");
        Matcher m2 = p1.matcher(idTxt.getText());
        try {
            if (!adiTxt.getText().isEmpty() && !soyTxt.getText().isEmpty() && !m.find() && !m1.find() && !m2.find() && !idTxt.getText().isEmpty()) {
                
                EntityManagerFactory emf = Persistence.createEntityManagerFactory("odev");
                EntityManager em = emf.createEntityManager();
                
                em.getTransaction().begin();
                Musteri musteri = new Musteri();
                musteri.setAdi(adiTxt.getText());
                musteri.setId(Integer.parseInt(idTxt.getText()));
                musteri.setSoyadi(soyTxt.getText());
                musteri.setBorc(0);
                
                em.persist(musteri);
                em.getTransaction().commit();
                em.close();
                emf.close();
                
            } else {
                JOptionPane.showMessageDialog(rootPane, "Lütfen ad,soyad kısımlarına rakam ve id kısımına harf girmeyiniz ve eksiksiz doldurunuz", "Hata", JOptionPane.WARNING_MESSAGE);
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Girilen id ile kayıtlı bir müşteri zaten var", "Hata", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_kayıtBtnActionPerformed

